package DAY5;

public class areaOfCircle {
    static void area() {
        final double PI = 3.142;
        int radius = 10;

        // Area of circle formula is PI * r * r
        double result = PI * radius * radius;
        System.out.println(result);

    }
    public static void main(String[] args) {
        area();
    }
}
